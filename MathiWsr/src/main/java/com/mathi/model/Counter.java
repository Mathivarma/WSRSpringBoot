package com.mathi.model;

public class Counter {
		private Integer ALLUS;
		private Integer UScount;
		private Integer DefectCount;
		public Integer getALLUS() {
			return ALLUS;
		}
		public void setALLUS(Integer aLLUS) {
			ALLUS = aLLUS;
		}
		public Integer getUScount() {
			return UScount;
		}
		public void setUScount(Integer uScount) {
			UScount = uScount;
		}
		public Integer getDefectCount() {
			return DefectCount;
		}
		public void setDefectCount(Integer defectCount) {
			DefectCount = defectCount;
		}
}
